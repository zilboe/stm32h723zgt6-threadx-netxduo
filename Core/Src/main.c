void tx_application_define(void *first_unused_memory)
{
    CHAR    *pointer = TX_NULL;
		UINT		result = TX_SUCCESS;
    /* Create a byte memory pool from which to allocate the thread stacks.  */
    result = tx_byte_pool_create(&POOL_BYTES, "Tot MEM Pool", memory_area, RTOS_BYTE_POOL_SIZE);
		if(result)
			thread_error();
		
    /* Allocate the stack for LED Thread.  */
    result = tx_byte_allocate(&POOL_BYTES, (VOID **) &pointer, RTOS_THREAD_STACK_SIZE, TX_NO_WAIT);
		if(result)
			thread_error();
		
    /* Create the main thread.  */
    result = tx_thread_create(&led_thread, "LED Thread", led_thread_entry, 0,  
                    pointer, RTOS_THREAD_STACK_SIZE, 
                    10, 10, TX_NO_TIME_SLICE, TX_AUTO_START);
		
		if(result)
			thread_error();
		
		/* Init NetXDUO */
		result = MX_NetXDuo_Init();
		if(result)
			thread_error();
		
}

void led_thread_entry(ULONG thread_input)
{
	uint8_t flag=0;
	while(1)
	{
		if(flag)
			HAL_GPIO_WritePin(GPIOG, GPIO_PIN_7, GPIO_PIN_RESET);
		else
			HAL_GPIO_WritePin(GPIOG, GPIO_PIN_7, GPIO_PIN_SET);
		flag = ~flag;
		tx_thread_sleep(1000);
	}
}

void _tx_thread_stack_error_handler(TX_THREAD *thread_ptr)
{
	thread_error();
}
